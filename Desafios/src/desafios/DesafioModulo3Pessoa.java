package desafios;

public class DesafioModulo3Pessoa {

	String nome;
	double peso;
	
	public DesafioModulo3Pessoa(String nome, double peso) {
		this.nome = nome;
		this.peso = peso;
	}
	
	
	double comer(DesafioModdulo3Comida comida) {
		
		return this.peso += comida.pesoComida;
		
	}
	
}
